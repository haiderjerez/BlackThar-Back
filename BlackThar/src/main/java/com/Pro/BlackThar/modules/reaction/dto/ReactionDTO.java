package com.Pro.BlackThar.modules.reaction.dto;

import com.Pro.BlackThar.modules.reaction.entity.Reaction.ReactionType;

public class ReactionDTO {
    private Long id;
    private ReactionType type;
    private Long userId;
    private Long postId;

    // Getters y Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public ReactionType getType() {
        return type;
    }

    public void setType(ReactionType type) {
        this.type = type;
    }

    public Long getUserId() {
        return userId;
    }

    public void setUserId(Long userId) {
        this.userId = userId;
    }

    public Long getPostId() {
        return postId;
    }

    public void setPostId(Long postId) {
        this.postId = postId;
    }
}